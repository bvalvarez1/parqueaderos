    @Test
    @Transactional
    void putNewPerson() throws Exception {
        // Initialize the database
        personRepository.saveAndFlush(person);

        int databaseSizeBeforeUpdate = personRepository.findAll().size();

        // Update the person
        Person updatedPerson = personRepository.findById(person.getId()).get();
        // Disconnect from session so that the updates on updatedPerson are not directly saved in db
        em.detach(updatedPerson);
        updatedPerson
            .fullName(UPDATED_FULL_NAME)
            .fistName(UPDATED_FIST_NAME)
            .surname(UPDATED_SURNAME)
            .email(UPDATED_EMAIL)
            .identificactionNumber(UPDATED_IDENTIFICACTION_NUMBER)
            .telephoneNumber(UPDATED_TELEPHONE_NUMBER);
        PersonDTO personDTO = personMapper.toDto(updatedPerson);

        restPersonMockMvc
            .perform(
                put(ENTITY_API_URL_ID, personDTO.getId())
                    .contentType(MediaType.APPLICATION_JSON)
                    .content(TestUtil.convertObjectToJsonBytes(personDTO))
            )
            .andExpect(status().isOk());

        // Validate the Person in the database
        List<Person> personList = personRepository.findAll();
        assertThat(personList).hasSize(databaseSizeBeforeUpdate);
        Person testPerson = personList.get(personList.size() - 1);
        assertThat(testPerson.getFullName()).isEqualTo(UPDATED_FULL_NAME);
        assertThat(testPerson.getFistName()).isEqualTo(UPDATED_FIST_NAME);
        assertThat(testPerson.getSurname()).isEqualTo(UPDATED_SURNAME);
        assertThat(testPerson.getEmail()).isEqualTo(UPDATED_EMAIL);
        assertThat(testPerson.getIdentificactionNumber()).isEqualTo(UPDATED_IDENTIFICACTION_NUMBER);
        assertThat(testPerson.getTelephoneNumber()).isEqualTo(UPDATED_TELEPHONE_NUMBER);
    }

