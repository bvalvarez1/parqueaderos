        restPersonMockMvc
            .perform(post(ENTITY_API_URL).contentType(MediaType.APPLICATION_JSON).content(TestUtil.convertObjectToJsonBytes(personDTO)))
            .andExpect(status().isCreated());

        // Validate the Person in the database
        List<Person> personList = personRepository.findAll();
        assertThat(personList).hasSize(databaseSizeBeforeCreate + 1);
        Person testPerson = personList.get(personList.size() - 1);
        assertThat(testPerson.getFullName()).isEqualTo(DEFAULT_FULL_NAME);
        assertThat(testPerson.getFistName()).isEqualTo(DEFAULT_FIST_NAME);
        assertThat(testPerson.getSurname()).isEqualTo(DEFAULT_SURNAME);
